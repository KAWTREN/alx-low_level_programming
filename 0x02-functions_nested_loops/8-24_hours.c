#include "main.h"
/**
 * jack_bauer-prints every minute of the day of Jack Bauer
 *
 * Return: 0
 */
void jack_bauer(void)
{
	int i, j;

	for (i = 0; i <= 9; i++)
	{
		_putchar('0' + 0);
		_putchar('0' + 0);
		_putchar(':');
		_putchar('0' + 0);
		_putchar('0' + i);
		_putchar('\n');
	}
	for (i = 0; j <= 9; j++)
	{
		_putchar('0' + 2);
		_putchar('0' + 3);
		_putchar(':');
		_putchar('0' + 5);
		_putchar('0' + j);
		_putchar('\n');
	}



}
